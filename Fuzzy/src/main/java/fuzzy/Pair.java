package fuzzy;

public class Pair {
    public int x,y,z; // chieu cao, can nang, duong huyet
                      // duong huyet, cholesterol, nhip tim
    public Pair() {

    }

    public Pair(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getZ() {
        return z;
    }

    public void setZ(int z) {
        this.z = z;
    }
}
